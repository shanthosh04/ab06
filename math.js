function Plus(a,b) {
    const result = a + b;
    return result;
}

function Minus(a,b) {
    const result = a - b;
    return result;
}

function Multi(a,b) {
    const result = a * b;
    return result;
}

function Divide(a,b) {
    const result = a / b;
    return result;
}

module.exports = { Plus, Minus, Multi, Divide }; 